public class User {

    public String fullName, age, email, phone, address;

    public User() {

    }

    public User (String fullName, String age, String email, String phone, String address) {
        this.fullName = fullName;
        this.age = age;
        this.email = email;
        this.phone = phone;
        this.address = address;
    }
}
